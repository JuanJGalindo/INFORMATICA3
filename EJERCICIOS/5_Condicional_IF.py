"""
Determine si un numero es mayor a 18, 
utilizando el condicional IF
"""

numero = 18
if numero > 18:
    print("Numero es mayor a 18")

"""
Determine si un numero es mayor o no a 18, 
utilizando el condicional IF-ELSE
"""
numero = 18
if numero > 18:
    print("Numero es mayor a 18")
else:
    print("Numero no es mayor a 18")


